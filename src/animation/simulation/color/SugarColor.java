package animation.simulation.color;

import javafx.scene.paint.Color;

public class SugarColor extends CellColor {
	
	/**
	 * Creates the correct color coordinations for sugar simulation
	 */
	public SugarColor() {
		super();
		putColor(0, Color.WHITE);
		putColor(1, Color.ANTIQUEWHITE);
		putColor(2, Color.BISQUE);
		putColor(3, Color.GOLD);
		putColor(4, Color.ORANGE);
		putColor(5, Color.CORAL);
		putColor(6, Color.DARKRED);
	}

}
